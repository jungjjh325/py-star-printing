"""
T = 4
   *
  ***
 *****
*******

3 1 3
2 3 2
1 5 1
0 7 0
"""
T = int(input())

for i in range(T):
    print(" " * (T - i - 1) + "*" * (2 * i + 1))
    
"""
" " * (T - i - 1)
->  첫 째줄 4 - 0 - 1 = 3
    둘 째줄 3 - 0 - 1 = 2
    셋 째줄 2 - 0 - 1 = 1
    넷 째줄 1 - 0 - 1 = 0
    으로 빈칸 생성

"*" * (2 * i + 1)
->  첫 째줄 2 * 0 + 1 = 1
    둘 째줄 2 * 1 + 1 = 3
    셋 째줄 2 * 2 + 1 = 5
    넷 째줄 2 * 3 + 1 = 7
    으로 별 찍기 생성
"""